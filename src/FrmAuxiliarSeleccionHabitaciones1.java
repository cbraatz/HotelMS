    private DefaultComboBoxModel modelocombosubtipo=null;
    private DefaultComboBoxModel modelocombosubtipo2=null;   //guarda el codigo
    private Subtipo _subTipo=null;
    private void CERRARSesion(){
        if(Sesion.isOpen()){
            Sesion.flush();
            Sesion.clear();
            Sesion.close();
        }
   }
    private void cargarcombosubtipo(){
        Transaction tx=null;
        Sesion=NewHibernateUtil.getSessionFactory().openSession();
        try{
            tx=Sesion.beginTransaction();
            modelocombosubtipo= new DefaultComboBoxModel();
            modelocombosubtipo2= new DefaultComboBoxModel();
            Query q=Sesion.createQuery("from Subtipo");
            List lis= q.list();
            if(q.list().size()>0){
                for (int i=0;i<lis.size();i++){
                    Subtipo suti=new Subtipo();
                    suti=(Subtipo)lis.get(i);
                    modelocombosubtipo.addElement(suti.getDescsubtipo());
                    modelocombosubtipo2.addElement(suti.getIdsubtipo());
                }
            }
            jComboBox1.setModel(modelocombosubtipo);
            tx.commit();
       }
       catch(HibernateException e){
           tx.rollback();
            e.printStackTrace();
       }
        CERRARSesion();
   }
    private void ubicarComboSubTipoEnPosicion(int cod){
        int lon=this.modelocombosubtipo2.getSize();
        for(int i=0;i<lon;i++){
            int codi=Integer.parseInt(modelocombosubtipo2.getElementAt(i).toString());  //busca el codigo del otro modelo que tiene los codigos
            if(codi==cod)
                this.jComboBox1.setSelectedIndex(i);  //ubica el combo en la posicion correcta
        }
   }
   private int obtenerCodigoActualDeComboSubTipo(){
        int resu=Integer.parseInt(modelocombosubtipo2.getElementAt(jComboBox1.getSelectedIndex()).toString());
        return resu;
   }
   public Subtipo obtenerObjetoSubTipoDeCombo(){
       Subtipo subti=null;
       try{
            Sesion = NewHibernateUtil.getSessionFactory().openSession();
            Transaction tx=Sesion.beginTransaction();
            subti=(Subtipo)Sesion.get(Subtipo.class, this.obtenerCodigoActualDeComboSubTipo());
            tx.commit();
            this.CERRARSesion();
       } catch (HibernateException e) {
           e.printStackTrace();
       }
       return subti;
   }
